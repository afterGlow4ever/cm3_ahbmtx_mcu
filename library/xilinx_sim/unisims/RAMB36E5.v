       end
       else begin
         if (WRITE_MODE_B_BIN == WRITE_MODE_B_READ_FIRST) begin
         if (~mem_arst_b) begin
         for (wb=0;wb<rd_loops_f;wb=wb+1) begin
           mem_rd_b_rf[wb]    = mem [rd_addr_b+wb];
           if (wb<rd_loops_f/8) begin
               memp_rd_b_rf[wb]        = memp [rd_addr_b/8+wb];
