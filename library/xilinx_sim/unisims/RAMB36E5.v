         if (WRITE_MODE_B_BIN == WRITE_MODE_B_WRITE_FIRST) begin
           for (rbw=0;rbw<rd_loops_f;rbw=rbw+1) begin
             mem_rd_b_wf[rbw]    = mem [rd_addr_b+rbw];
