            memp_a_lat  <= #100 memp_rd_b_rf;
          end
          else begin
            mem_a_lat   <= #100 mem_rd_a;
            memp_a_lat  <= #100 memp_rd_a;
          end
        end
         first_read <= 1'b1;
      end
